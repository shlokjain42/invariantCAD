type FixtureVersion = 1 | 2;
type Mode = "check" | "write";

interface Options {
  readonly help: boolean;
  readonly mode: Mode;
  readonly version?: FixtureVersion;
}

interface EncodedFixture {
  readonly bytes: Uint8Array;
  readonly capabilities: KernelShapeArtifactCapabilities;
  readonly semanticWitness: string;
  readonly version: FixtureVersion;
}

