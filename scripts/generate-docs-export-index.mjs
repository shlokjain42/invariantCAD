import { readFile, writeFile } from "node:fs/promises";
import process from "node:process";

const root = new URL("../", import.meta.url);
const outputUrl = new URL("../docs/reference/export-index.mdx", import.meta.url);
const entries = Object.freeze([
  Object.freeze({ packageName: "invariantcad", source: "src/index.ts" }),
  Object.freeze({
    packageName: "invariantcad/conformance",
    source: "src/conformance.ts",
  }),
  Object.freeze({
    packageName: "invariantcad/kernels/occt",
    source: "src/occt-kernel.ts",
  }),
]);

function collectNamedExports(sourceText, sourcePath) {
  const exports = [];

  // Public entry points intentionally use explicit named exports. Parsing that
  // constrained syntax keeps this script independent of TypeScript's compiler
  // API, which changed in TypeScript 7.
  const blockPattern = /^export\s+(type\s+)?\{([\s\S]*?)\}\s*(?:from\s+["']([^"']+)["'])?\s*;/gm;
  for (const match of sourceText.matchAll(blockPattern)) {
    const blockIsTypeOnly = match[1] !== undefined;
    const module = match[3] ?? "local";
    const uncommented = match[2]
      .replace(/\/\*[\s\S]*?\*\//g, "")
      .replace(/\/\/.*$/gm, "");
    for (const rawSpecifier of uncommented.split(",")) {
      const specifier = rawSpecifier.trim();
      if (specifier === "") continue;
      const specifierMatch = /^(type\s+)?([A-Za-z_$][\w$]*)(?:\s+as\s+([A-Za-z_$][\w$]*))?$/.exec(
        specifier,
      );
      if (specifierMatch === null) {
        throw new Error(`${sourcePath} has an unsupported export: ${specifier}`);
      }
      exports.push({
        name: specifierMatch[3] ?? specifierMatch[2],
        kind: blockIsTypeOnly || specifierMatch[1] !== undefined ? "type" : "value",
        source: module,
      });
    }
  }

  const declarationPattern =
    /^export\s+(?:(?:declare|abstract|async)\s+)*(const|let|var|class|function|interface|type|enum)\s+([A-Za-z_$][\w$]*)/gm;
  for (const match of sourceText.matchAll(declarationPattern)) {
    const declaration = match[1];
    exports.push({
      name: match[2],
      kind:
        declaration === "interface" || declaration === "type"
          ? declaration
          : declaration === "class" || declaration === "function" || declaration === "enum"
            ? declaration
            : "value",
      source: "local",
    });
  }

  for (const match of sourceText.matchAll(/^export\s+([^\n]+)/gm)) {
    const prefix = match[1].trimStart();
    if (
      prefix.startsWith("{") ||
      prefix.startsWith("type {") ||
      /^(?:(?:declare|abstract|async)\s+)*(?:const|let|var|class|function|interface|type|enum)\b/.test(
        prefix,
      )
    ) {
      continue;
    }
    throw new Error(
      `${sourcePath} contains an unsupported export declaration: export ${prefix}`,
    );
  }

  const unique = new Map();
  for (const item of exports) {
    if (unique.has(item.name)) {
      throw new Error(`${sourcePath} exports '${item.name}' more than once`);
    }
    unique.set(item.name, item);
  }
  return [...unique.values()].sort((first, second) =>
    first.name.localeCompare(second.name),
  );
}

const inventory = [];
for (const entry of entries) {
  const sourceUrl = new URL(entry.source, root);
  const sourceText = await readFile(sourceUrl, "utf8");
  inventory.push({
    ...entry,
    exports: collectNamedExports(sourceText, entry.source),
  });
}

const lines = [
  "---",
  'title: "Export index"',
  'description: "Generated alphabetical inventory of every named export from InvariantCAD\'s supported package entry points."',
  'icon: "list-ordered"',
  "---",
  "",
  "{/* Generated by scripts/generate-docs-export-index.mjs. Do not edit by hand. */}",
  "",
  "This page inventories every named export from the three supported package",
  "entry points. It is generated from source and checked during CI so a new",
  "public symbol cannot remain invisible to the documentation.",
  "",
  "`value` means the name can be imported at runtime. `type`, `interface`, and",
  "other declaration kinds describe TypeScript-only or declaration-specific",
  "exports. Versioned historical names are intentionally included.",
  "",
];

for (const entry of inventory) {
  lines.push(
    `## \`${entry.packageName}\``,
    "",
    `**${entry.exports.length} named exports** from \`${entry.source}\`.`,
    "",
    "| Name | Kind | Declared/re-exported from |",
    "| --- | --- | --- |",
  );
  for (const item of entry.exports) {
    lines.push(`| \`${item.name}\` | ${item.kind} | \`${item.source}\` |`);
  }
  lines.push("");
}

lines.push(
  "## Import policy",
  "",
  "Only the entry points above and `invariantcad/package.json` are public.",
  "Internal `dist/` chunks and vendored runtime files are implementation details.",
  "See [package exports](/reference/package-exports) for compatibility rules.",
);

const generated = `${lines.join("\n")}\n`;
if (process.argv.includes("--check")) {
  let current;
  try {
    current = await readFile(outputUrl, "utf8");
  } catch {
    current = undefined;
  }
  if (current !== generated) {
    console.error(
      "The generated export index is stale. Run pnpm docs:generate and commit it.",
    );
    process.exit(1);
  }
  console.log("Generated export index is current.");
} else {
  await writeFile(outputUrl, generated);
  console.log(
    `Generated ${inventory.reduce((count, entry) => count + entry.exports.length, 0)} documented exports.`,
  );
}
